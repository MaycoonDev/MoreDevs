package Atv03;

public class Portador {
    private int id;
    private String nome;

    public Portador() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String portador) {
        this.nome = portador;
    }
}
